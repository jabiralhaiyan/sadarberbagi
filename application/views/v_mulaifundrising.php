              <h3><i class="fa fa-check-square-o"></i> Apa saja fundraising yang boleh dilakukan di sadarberbagi?</h3>
              <p>Banyak! Untuk Anda, orang baik yang ingin berkontribusi tapi terkendala waktu, Anda dapat dengan mudah menjadi relawan fundraising dan menggalang dana untuk yayasan dan organisasi terdaftar di Sadarberbagi. Jika Anda memiliki inisiatif sendiri, kami mengakomodir berbagai jenis kebutuhan fundraising dari mulai menolong teman yang butuh biaya pengobatan, patungan menggelar acara komunitas, sampai menciptakan karya seperti buku, film dan produk inovasi. Yang tidak kami akomodir adalah fundraising untuk meminta kebutuhan pribadi dan kegiatan apapun yang melanggar hukum NKRI.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Apakah ada proses seleksi terhadap proposal fundraising yang masuk?</h3>
              <p>Selain fundraising yang ditujukan untuk membantu yayasan dan organisasi terdaftar, setiap proposal fundraising yang masuk ke Sadarberbagi akan di cek 1. Identitias individu/organisasi pihak yang akan melakukan fundraising dan 2. Kelayakan informasi yang disediakan. Proposal yang kami tolak cenderung tidak lolos seleksi karena informasi yang disediakan tidak cukup dan tidak meyakinkan. Penting untuk melengkapi form yang ada dengan informasi yang lengkap dan transparan.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Bayar ngga untuk fundraising di Sadarberbagi?</h3>
              <p>Anda bisa membuat halaman fundraising online Anda secara gratis. Kami mengenakan biaya administrasi sebesar 5% dari total donasi yang terkumpul, kecuali untuk fundraising bencana alam dan darurat medis (0%). Sebagai social enterprise Sadarberbagi menggunakan fee ini sebagai penunjang kebutuhan operasional.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Apa yang terjadi kalau donasi tidak mencapai target?</h3>
              <p>Kami menerapkan sistem 'flexible funding', artinya, Anda tetap berhak menerima donasi yang terkumpul meski tidak mencapai target yang ditentukan. Pencairan donasi akan dilakukan disaat periode fundraising Anda telah berakhir.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Bagaimana agar saya dapat banyak donatur?</h3>
              <p>Berdasarkan statistik, 40% donatur pertama berasal dari kenalan/lingkaran terdekat pemilik kampanye, 30% kedua berasal dari "temannya teman" dan 30% berikutnya barulah orang yang tidak dikenal. Artinya, anda harus meyakinkan dulu orang terdekat sebelum menjangkau masyarakat yang lebih luas. Simak tips-tips fundraising jitu di blog kami.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Bagaimana cara mencairkan donasi yang terkumpul?</h3>
              <p>Sebelum dana kami transfer Anda wajib memberikan update berisikan informasi dana terkumpul, serta rencana/laporan pelaksanaan program. Update ini akan terkirim otomatis ke seluruh donatur, dan jika donasi sudah digunakan Anda wajib untuk melakukan update kembali berisikan laporan penggunaan dana.</p><br>

              <h3><i class="fa fa-check-square-o"></i> Apa saja proses untuk mulai fundraising di Sadarberbagi?</h3>
              <p>1. Seleksi proposal: max. 2 hari kerja setelah proposal di submit.<br>
                2. Periode fundraising: fleksibel, ditentukan oleh masing-masing pemilik kampanye.<br>
                3. Pencairan dana: max. 2 hari kerja setelah kampanye berakhir.</p><br>

